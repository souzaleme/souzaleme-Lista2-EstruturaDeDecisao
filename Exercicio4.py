'''Faça um Programa que verifique se uma letra digitada é vogal ou consoante.'''

letra = str(input('Digite uma letra: ')).lower()

if letra == 'a':
    print('Vogal')
elif letra == 'e':
    print('Vogal')
elif letra == 'i':
    print('Vogal')
elif letra == 'o':
    print('Vogal')
elif letra == 'u':
    print('Vogal')
else:
    print('Consoante')